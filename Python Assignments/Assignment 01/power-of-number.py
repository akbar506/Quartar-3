"""
Calculate Powers of Numbers.

- Task: Given two integers base and exponent
 - Compute base raised to the power of exponent.

 - base:int = 3
 - exponent:int = 4

- Expected Output:
 - Power result: 81
"""

base:int = 3
exponent:int = 4

power: int = base**exponent # Output: Power result: 81

print(f"Power result: {power}")